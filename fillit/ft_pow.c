#include "fillit.h"

uint64_t	ft_pow(uint64_t x, uint64_t y)
{
	uint64_t i;

	i = x;
	if (y == 0)
		return (1);
	while (y > 1)
	{
		x *= i;
		y--;
	}
	return (x);
}
